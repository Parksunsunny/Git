package day1;

public class Hello 
{
	public static void main(String[] args) 
	{
		// 한줄에 나란히 출력
		System.out.print("Hello 출력합니다. - 이클립스");
		
		// 한줄씩 출력
		System.out.println("두번째 출 출력합니다.");
		
		// 숫자, 논리값은 "없이 출력가능
		System.out.println(100);
		System.out.println(true);
		System.out.println(3.14);
		
		System.out.println(100 + 3.14 + "여러줄을 출력합니다.");
		
		System.out.printf("%d %f %s", 100, 3.14, "여러줄 출력");
		
	}
}
 